// operadores aritmeticos
// suma
console.log(5 + 4);
// resta
console.log(5 - 4);
// multiplicacion
console.log(5 * 4);
// division
console.log(5 / 4);
// Modulo da residuo residuo
console.log(5 % 4);
// incremento suma un numero
let numero = 6;
console.log(++numero);
//decremento resta un numero
let otroNumero = 10;
console.log(--otroNumero);
// comparacion simple
console.log( 5 == '5'); // arroja un valor booleano tiene el mismo valor es true
// comparacion estricta
// == signo de igualadad
console.log( 5 === '5');// arroja un valor booleano tiene el mismo valor pero el tipo de dato no es un string  es false
// desigualdad simple
// === signo de igualadad
console.log( 5 != '5');// valor desigual 5 es distinto que '5'
// desigualdad estricta
console.log( 5 !== '5'); // valor y tipo de dato desigual
// mayor que
console.log( 10 > 6 );
// mayor o igual que
console.log( 6 >= 6);
// menor que
console.log( 7 < 7);
// menor o gual que
console.log( 7 <= 7);
// AND && por solo una falsa sera false
console.log( true && true );
console.log( false && true );
// OR || si solo una es verdadera sera true
console.log( true || true );
console.log( false || true );
// concatenacion
let nombre = 'Maye';
let apellido = 'Bueno';
console.log( nombre +' '+ apellido );
